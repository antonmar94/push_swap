#include "libft.h"

void	*ft_memccpy(void *dest, void *src, int c, size_t size)
{
	size_t			i;
	unsigned char	*d;
	unsigned char	*s;

	i = 0;
	d = (unsigned char *)dest;
	s = (unsigned char *)src;
	while (i < size)
	{
		d[i] = s[i];
		if (d[i] == (unsigned char)c)
			return (&d[i + 1]);
		i++;
	}
	return (0);
}
