#include "libft.h"

void	ft_lstadd_front(t_list **alst, t_list *new)
{
	if (!new)
		return ;
	if (!alst)
	{
		new->next = NULL;
		*alst = new;
	}
	new->next = (*alst);
	*alst = new;
}
